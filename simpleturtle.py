import turtle

# Create a turtle screen
screen = turtle.Screen()

# Create a turtle
my_turtle = turtle.Turtle()

# Move the turtle forward and turn right
my_turtle.forward(100)
my_turtle.right(90)

# Move the turtle forward and turn right again
my_turtle.forward(100)
my_turtle.right(90)

# Move the turtle forward and turn right again
my_turtle.forward(100)
my_turtle.right(90)

# Move the turtle forward and turn right again to complete the square
my_turtle.forward(100)
my_turtle.right(90)

# Close the turtle graphics window
turtle.done()
